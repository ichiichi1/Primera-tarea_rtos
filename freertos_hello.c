/* FreeRTOS kernel includes. */
#include "FreeRTOS.h"
#include "task.h"
#include "queue.h"
#include "timers.h"

/* Freescale includes. */
#include "fsl_device_registers.h"
#include "fsl_debug_console.h"
#include "board.h"

#include "pin_mux.h"
#include "clock_config.h"
/*******************************************************************************
 * Definitions
 ******************************************************************************/

/* Task priorities. */
#define hello_task_PRIORITY (configMAX_PRIORITIES - 1)
/*******************************************************************************
 * Prototypes
 ******************************************************************************/
static void primera(void *pvParameters);
static void segunda(void *pvParameters);
static void tercera(void *pvParameters);

/*******************************************************************************
 * Code
 ******************************************************************************/
/*!
 * @brief Application entry point.
 */
int main(void)
{
    /* Init board hardware. */
    BOARD_InitPins();
    BOARD_BootClockRUN();
    BOARD_InitDebugConsole();

    xTaskCreate(primera, "PRIMERA", configMINIMAL_STACK_SIZE + 30, NULL, 1, NULL);
    xTaskCreate(segunda, "SEGUNDA", configMINIMAL_STACK_SIZE + 30, NULL, 2, NULL);
    xTaskCreate(tercera, "TERCERA", configMINIMAL_STACK_SIZE + 30, NULL, 1, NULL);

    vTaskStartScheduler();
    for (;;)
        ;
}

/*!
 * @brief Task responsible for printing of "Hello world." message.
 */
static void primera(void *pvParameters)
{

        PRINTF("hola\r\n");
        vTaskSuspend(NULL);
        vTaskDelay(1000000);
}
static void segunda(void *pvParameters)
{

        PRINTF("que .\r\n");
        vTaskDelay(10000);

}
static void tercera(void *pvParameters)
{

        PRINTF("hace.\r\n");
        vTaskDelay(1000);
}
